package main

import "fmt"

func main() {
	// 二维数组
	var a = [2][4]uint8{
		{12, 13, 14, 15},
		{16, 17, 18, 19},
	}
	// 输出各元素
	fmt.Println("----- 二维数组中的元素 -----")
	for i := 0; i < 2; i++ {
		for j := 0; j < 4; j++ {
			fmt.Printf("%d  ", a[i][j])
		}
		fmt.Println()		// 换行
	}

	fmt.Println()

	// 三维数组
	var b = [5][4][3]int32{
		{
			{1, 2, 3},
			{7, 8, 9},
			{12, 15, 18},
			{25, 26, 27},
		},
		{
			{-2, -3, -6},
			{-20, 35, -7},
			{60, 62, 64},
			{-100, -101, -102},
		},
		{
			{65, 66, 67},
			{305, 405, 505},
			{125, 135, 145},
			{-6, -17, 810},
		},
		{
			{2200, 130, -96},
			{-72, 160, 400},
			{215, -76, -320},
			{57, 58, 59},
		},
		{
			{8850, 3756, 418},
			{-600, -520, 307},
			{2125, 1102, -4720},
			{-595, -116, 907},
		},
	}
	fmt.Println("----- 三维数组中的元素 -----")
	for i := 0; i < 5; i++ {
		for j := 0; j < 4; j++ {
			for k := 0; k < 3; k++ {
				fmt.Printf("%-8d", b[i][j][k])
			}
			fmt.Print("\n")			// 换行
		}
		fmt.Print("\n\n")			// 换行
	}
}